            <h2 class="featurette-heading">Billing <span class="text-muted" style="font-size: 24px;">Trust me and you</span></h2>
            <p class="lead">Billing is one of the most important process when importing or exporting a cargo. Without billing, no consignee can ship
        a cargo locally or internationally. Billing, which also is referred to as a bill of lading, is a legal contract between the shipper and the carrier.
         The billing includes the necessary details of the transaction and typically is signed by the shipper and the carrier. It is used by accounting personnel as support documentation to justify billing the shipper or the receiver, depending on the terms of the contract, for the successful delivery of goods.

    <br>  
    <br>
   While the information included on a freight bill varies among companies, certain details are usually included. The details include the name and contact information for the shipper and the receiver as well as a description of the goods shipped. The description of the goods shipped includes the quantity and type of goods. The carrier needs to know what it transports, and federal, state and local laws govern what can be shipped and how it can be shipped.</p>
